console.log('For');
for (let i = 1; i <= 10; i++)
    console.log(i);
//While loop
let n: number = 10;
let i: number = 1;

console.log('While');
while (i <= n) {
    console.log(i);
    i++;
}

i = 1;
//do while
console.log('Do While');
do {
    console.log(i);
    i++;
} while (i <= n);