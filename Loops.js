//for loop(definite)
console.log('For');
for (var i_1 = 1; i_1 <= 10; i_1++)
    console.log(i_1);
//While loop
var n = 10;
var i = 1;
console.log('While');
while (i <= n) {
    console.log(i);
    i++;
}
i = 1;
//do while
console.log('Do While');
do {
    console.log(i);
    i++;
} while (i <= n);
